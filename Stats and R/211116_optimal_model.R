# Аргумент direction позволяет задать направление поиска.

# Первый аргумент (object) задаёт начальную модель, с которой начинается поиск. Обратите внимание на то, что при разных 
# значениях аргумента direction нужно использовать разные начальные модели. Функция step возвращает оптимальную модель.
 
# Итак, задача! C помощью функции step найдите оптимальную модель для предсказания rating в датасете attitude. 
