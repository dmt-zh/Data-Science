# переменных в данном датасете.
 
# model_null - модель, в которой нет ни одного предиктора, а есть только intercept. Значение intercept - это просто
# среднее значение зависимой переменной. Соответственно, модель предоставляет нам информацию только о том, отличается
# ли это среднее от нуля.
# 
# Как говорилось в лекции, функция step позволяет нам подобрать модель с оптимальным количеством предикторов. С помощью 
# аргумента scope мы можем задать пространство моделей с разным числом предикторов, в котором будет происходить поиск 
# оптимального набора предикторов. Самый простой путь - задать границы возможных моделей с помощью нулевой и полной моделей.

